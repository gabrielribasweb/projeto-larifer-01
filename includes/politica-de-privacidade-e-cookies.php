  <h2 class="titulo">Política de Cookies</h2>
  <p>Aqui, você encontrará informações objetivas e claras sobre o que são Cookies, quais Cookies utilizamos em nossas aplicações, qual papel desempenham e como configurá-los. </p>
  <div class="topico">1. O que são Cookies?</div>
  <p>Cookies são pequenos arquivos de texto ou fragmentos de informação que são baixados em seu computador, smartphone ou qualquer outro dispositivo com acesso à internet quando você visita nossa aplicação.</p>
  <p>Eles contêm informações sobre a sua navegação em nossas páginas e retêm apenas informações relacionadas a suas preferências.</p>
  <p>Assim, essa página consegue armazenar e recuperar os dados sobre os seus hábitos de navegação, de forma a melhorar a experiência de uso, por exemplo. É importante frisar que eles não contêm informações pessoais específicas, como dados sensíveis ou bancários.</p>
  <p>O seu navegador armazena os cookies no disco rígido, mas ocupam um espaço de memória mínimo, que não afetam o desempenho do seu computador. A maioria das informações são apagadas logo ao encerrar a sessão, como você verá no próximo tópico.</p>
  <div class="topico">1.1. Tipos de Cookies</div>
  <p>Os cookies, quanto a seus proprietários, podem ser:</p>
  <p>
  <ul>
    <li>Cookies proprietários: são cookies definidos por nós ou por terceiros em nosso nome</li>
    <li>Cookies de terceiros: são cookies definidos por terceiros confiáveis em nossa aplicação</li>
  </ul>
  </p>
  <p>Os cookies, quanto ao seu tempo de vida, podem ser:</p>
  <p>
  <ul>
    <li>Cookies de sessão ou temporários: são cookies que expiram assim que você fecha o seu navegador, encerrando a sessão.</li>
    <li>Cookies persistentes ou permanentes: são cookies que permanecem no seu dispositivo durante um período determinado ou até que você os exclua.</li>
  </ul>
  </p>
  <p>Os cookies, quanto a sua finalidade, podem ser:</p>
  <p>
  <ul>
    <li>Cookies necessários: são cookies essenciais que possibilitam a navegação em nossas aplicações e o acesso a todos os recursos; se estes, nossos serviços podem apresentar mal desempenho ou não funcionar.</li>
    <li>Cookies de desempenho: são cookies que otimizam a forma que nossas aplicações funcionam, coletando informações anônimas sobre as páginas acessadas.</li>
    <li>Cookies de funcionalidade: são cookies que memorizam suas preferências e escolhas (como seu nome de usuário)</li>
    <li>Cookies de publicidade: são cookies que direcionam anúncios em função dos seus interesses e limitam a quantidade de vezes que o anúncio aparece.</li>
  </ul>
  </p>
